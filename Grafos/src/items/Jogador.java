package items;

public class Jogador {
	private String nome;
	private String pecacor;
	private int life;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPecacor() {
		return pecacor;
	}
	public void setPecacor(String pecacor) {
		this.pecacor = pecacor;
	}
	public int getLife() {
		return life;
	}
	public void setLife(int life) {
		this.life = life;
	}

}
